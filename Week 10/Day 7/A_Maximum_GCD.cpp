#include <bits/stdc++.h>
using namespace std;
int GCD(int a, int b)
{
    return __gcd(a, b);
}
int main()
{
    ios::sync_with_stdio(false);
    cin.tie(nullptr);
    int t;
    cin >> t;
    while (t--)
    {
        int n;
        cin >> n;
        int l = n / 2;
        cout << l << endl;
    }

    return 0;
}